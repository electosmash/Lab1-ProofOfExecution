                if(i > (10 / 2) - 1) { d = ((i+1) - (10 / 2))-1; }

                for (int a = 0; a < d; a++) { newS += "-"; }

                de = Math.Abs(d - 5);
                //listboxB.Items.Add(de + "-vs-" + d);
